public class TestCharEscapes {
    public static void main(String[] args) {
        char newline = '\n';
        char tab = '\t';
        char backslash = '\\';
        char quote = '\'';
        System.out.println((int)newline);
        System.out.println((int)tab);
        System.out.println((int)backslash);
        System.out.println((int)quote);
    }
}