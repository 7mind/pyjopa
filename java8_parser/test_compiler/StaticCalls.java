public class StaticCalls {
    public static void main(String[] args) {
        int x = -42;
        int absX = Math.abs(x);
        System.out.println(absX);

        int maxVal = Math.max(10, 20);
        System.out.println(maxVal);

        int minVal = Math.min(10, 20);
        System.out.println(minVal);
    }
}
